public class datatype {
public static void main(String[] args) {
       // data type 
         int num;
         num = 5;
        System.out.println(num + " is num");
       // for loop
         for(int i = 0; i < 5; i++){
            System.out.println("hello");
        }

        // for each loop 
         int arr[] = {1, 4, 5,6};
        for(int i : arr){
            System.out.println(i);
        }
     }


}